"use client";

import React from 'react';
import NextLink from 'next/link';
import { cn } from '@/lib/utils';
import { ExternalLink as ExternalLinkIcon } from 'lucide-react';

export interface LinkProps extends Omit<React.AnchorHTMLAttributes<HTMLAnchorElement>, 'href'> {
  href: string;
  variant?: 'default' | 'underline' | 'button' | 'subtle';
  external?: boolean;
  showExternalIcon?: boolean;
  openInNewTab?: boolean;
  className?: string;
  children: React.ReactNode;
}

const variantConfig = {
  default: 'text-blue-400 hover:text-blue-300 transition-colors',
  underline: 'text-blue-400 hover:text-blue-300 underline underline-offset-2 transition-colors',
  button: 'inline-flex items-center justify-center px-4 py-2 bg-blue-600 hover:bg-blue-700 text-white rounded-lg font-medium transition-colors',
  subtle: 'text-white/80 hover:text-white transition-colors'
};

export const Link: React.FC<LinkProps> = ({
  href,
  variant = 'default',
  external = false,
  showExternalIcon = false,
  openInNewTab = false,
  className,
  children,
  ...props
}) => {
  const isExternal = external || href.startsWith('http') || href.startsWith('mailto:') || href.startsWith('tel:');
  const shouldOpenInNewTab = openInNewTab || isExternal;

  const linkProps = {
    className: cn(
      variantConfig[variant],
      "inline-flex items-center gap-1",
      className
    ),
    ...(shouldOpenInNewTab && {
      target: '_blank',
      rel: 'noopener noreferrer'
    }),
    ...props
  };

  const content = (
    <>
      {children}
      {showExternalIcon && isExternal && (
        <ExternalLinkIcon className="h-3.5 w-3.5" />
      )}
    </>
  );

  if (isExternal) {
    return (
      <a href={href} {...linkProps}>
        {content}
      </a>
    );
  }

  return (
    <NextLink href={href} {...linkProps}>
      {content}
    </NextLink>
  );
};

// Navigation Link (active state support)
export interface NavLinkProps extends LinkProps {
  isActive?: boolean;
  activeClassName?: string;
}

export const NavLink: React.FC<NavLinkProps> = ({
  isActive = false,
  activeClassName = 'text-blue-400 font-semibold',
  className,
  ...props
}) => {
  return (
    <Link
      className={cn(
        className,
        isActive && activeClassName
      )}
      {...props}
    />
  );
};

// Breadcrumb Link
export interface BreadcrumbLinkProps extends Omit<LinkProps, 'variant'> {
  isLast?: boolean;
}

export const BreadcrumbLink: React.FC<BreadcrumbLinkProps> = ({
  isLast = false,
  className,
  ...props
}) => {
  if (isLast) {
    return (
      <span className={cn("text-white font-medium", className)}>
        {props.children}
      </span>
    );
  }

  return (
    <Link
      variant="subtle"
      className={cn("hover:text-blue-400", className)}
      {...props}
    />
  );
};

